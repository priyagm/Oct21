package com.aspire.java.logical;

public class Stringswap
{
public static void main(String[] args)
{
	String s1="priyanka";
	String s2="manhallikar";
	String temp="";
	temp=s1;// temp=priyanka
	s1=s2;// s1=manhallikar
	s2=temp;// s2=priyanka
	
	
	
	
	System.out.println(temp);
	System.out.println(s1);
	
}
}

package com.aspire.java.LogicalStarProg;
//*****
//***** row=5; column =5
//***** row =i; column =;
//*****
//*****
public class StarSquare5Row5Column 
{
 public static void main(String[] args)
 {
	for(int i=1; i<=5;i++)
	{
		for(int j=1;j<=5;j++)
		{
			System.out.print("*");
		}
		System.out.println();
	}
	
}
}
